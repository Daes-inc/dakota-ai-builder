function safeText(value, fallback = "website") {
  return String(value || fallback)
    .replace(/[<>]/g, "")
    .trim() || fallback
}

function slugify(value) {
  return safeText(value)
    .toLowerCase()
    .replace(/[^a-z0-9\s-]/g, "")
    .trim()
    .replace(/\s+/g, "-")
}

