import express from "express";
import cors from "cors";

const app = express();
const PORT = process.env.PORT || 3000;

app.use(cors());
app.use(express.json());

app.get("/health", (req, res) => {
  res.json({
    status: "online",
    system: "Dakota AI Builder",
    version: "1.0"
  });
});

app.get("/", (req, res) => {
  res.send(`
    <!DOCTYPE html>
    <html>
    <head>
      <title>Dakota AI Builder</title>
      <style>
        body {
          background: #0f172a;
          color: white;
          font-family: Arial, sans-serif;
          text-align: center;
          padding: 40px;
          margin: 0;
        }

        .card {
          background: #1e293b;
          padding: 30px;
          border-radius: 12px;
          max-width: 700px;
          margin: 40px auto;
          box-shadow: 0 10px 30px rgba(0,0,0,0.25);
        }

        button {
          background: #22c55e;
          border: none;
          padding: 12px 20px;
          border-radius: 8px;
          color: white;
          font-size: 18px;
          cursor: pointer;
        }

        pre {
          text-align: left;
          white-space: pre-wrap;
          word-break: break-word;
          background: #0b1220;
          padding: 16px;
          border-radius: 8px;
          margin-top: 20px;
          overflow-x: auto;
        }
      </style>
    </head>
    <body>
      <div class="card">
        <h1>Dakota AI Builder</h1>
        <p>Automated AI App Builder System</p>
        <button onclick="buildSite()">Build Example App</button>
        <pre id="result">Ready.</pre>
      </div>

      <script>
        async function buildSite() {
          const res = await fetch('/create-app', {
            method: 'POST',
            headers: { 'Content-Type': 'application/json' },
            body: JSON.stringify({
              prompt: 'affiliate comparison site'
            })
          });

          const data = await res.json();
          document.getElementById('result').textContent =
            JSON.stringify(data, null, 2);
        }
      </script>
    </body>
    </html>
  `);
});

app.post("/create-app", (req, res) => {
  const prompt = (req.body?.prompt || "website").trim();
  const projectName = prompt.replace(/\s+/g, "-").toLowerCase();

  const files = {
    "index.html": `
<!DOCTYPE html>
<html>
<head>
  <title>${prompt}</title>
  <style>
    body {
      font-family: Arial, sans-serif;
      background: #0f172a;
      color: white;
      text-align: center;
      padding: 40px;
      margin: 0;
    }

    button {
      background: #22c55e;
      border: none;
      padding: 12px 20px;
      border-radius: 8px;
      color: white;
      font-size: 18px;
      cursor: pointer;
    }
  </style>
</head>
<body>
  <h1>${prompt}</h1>
  <p>Generated by Dakota AI Builder</p>
  <button>Start</button>
</body>
</html>
`
  };

  res.json({
    status: "success",
    projectName,
    prompt,
    files
  });
});

app.listen(PORT, () => {
  console.log("Dakota AI Builder running on port", PORT);
});