app.get("/site", (req, res) => {
  const prompt = req.query.prompt || "website"
  const project = buildProject(prompt)

  const html = project.files["index.html"]
    .replace(
      `</head>`,
      `<style>${project.files["style.css"]}</style></head>`
    )
    .replace(
      `</body>`,
      `<script>${project.files["script.js"]}</script></body>`
    )

  res.send(html)
})

